# Comparativo

resultado = 2 == 2
print("O valor é:", resultado)
print(type(resultado))

# Diferença

resultado = 2 != 2
print("O valor é:", resultado)
print(type(resultado))

# Maior

resultado = 2 > 2
print("O valor é:", resultado)
print(type(resultado))

# Menor

resultado = 2 < 2
print("O valor é:", resultado)
print(type(resultado))

# Maior ou Igual

resultado = 2 >= 2
print("O valor é:", resultado)
print(type(resultado))

# Menor ou Igual

resultado = 2 <= 2
print("O valor é:", resultado)
print(type(resultado))
